/**
 * Header used to propagate JWT tokens between services.
 */
export const AUTH_HEADER = 'authorization';

/**
 * Header used to propagate the authenticated user's ID.
 */
export const USER_ID_HEADER = 'x-sentinel-user-id';

/**
 * Header used to propagate the authenticated user's roles.
 */
export const USER_ROLES_HEADER = 'x-sentinel-user-roles';

/**
 * Header used to propagate the user's classification level.
 */
export const USER_CLASSIFICATION_HEADER = 'x-sentinel-classification';
